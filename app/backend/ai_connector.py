def post_json(config: Dict[str, Any], payload: Dict[str, Any]) -> Dict[str, Any]:
    endpoint = normalize_endpoint_url(str(config.get("endpoint") or "").strip())
    if not endpoint:
        raise ValueError("API endpoint is empty")
    data = json.dumps(payload, ensure_ascii=False).encode("utf-8")
    headers = {
        "Content-Type": "application/json; charset=utf-8",
        "Accept": "application/json, text/plain, */*",
    }
    headers.update(parse_custom_headers(str(config.get("customHeaders") or "")))
    api_key = str(config.get("apiKey") or "").strip()
    key_header = str(config.get("apiKeyHeader") or "Authorization")
    if api_key and key_header != "none":
        prefix = str(config.get("apiKeyPrefix") or "")
        headers[key_header] = f"{prefix}{api_key}" if prefix else api_key
    timeout = effective_timeout(endpoint, config)
    direct_first = is_local_endpoint(endpoint)
    try:
        raw, content_type = execute_post(endpoint, data, headers, timeout, direct=direct_first)
    except urllib.error.HTTPError as exc:
        raw = exc.read().decode("utf-8", errors="replace")
        raise RuntimeError(f"API HTTP {exc.code}: {raw[:1000]}") from exc
    except (TimeoutError, socket.timeout) as exc:
        raise RuntimeError(format_timeout_error(endpoint, timeout)) from exc
    except urllib.error.URLError as exc:
        if is_timeout_error(exc):
            raise RuntimeError(format_timeout_error(endpoint, timeout)) from exc
        if should_retry_without_proxy(endpoint, exc):
            try:
                raw, content_type = execute_post(endpoint, data, headers, timeout, direct=True)
            except urllib.error.HTTPError as direct_http:
                raw = direct_http.read().decode("utf-8", errors="replace")
                raise RuntimeError(f"API HTTP {direct_http.code}: {raw[:1000]}") from direct_http
            except (TimeoutError, socket.timeout) as direct_timeout:
                raise RuntimeError(format_timeout_error(endpoint, timeout, retried_direct=True)) from direct_timeout
            except urllib.error.URLError as direct_exc:
                if is_timeout_error(direct_exc):
                    raise RuntimeError(format_timeout_error(endpoint, timeout, retried_direct=True)) from direct_exc
                raise RuntimeError(format_url_error(endpoint, direct_exc, retried_direct=True)) from direct_exc
        else:
            raise RuntimeError(format_url_error(endpoint, exc, retried_direct=False)) from exc

    if "json" in content_type.lower():
        return json.loads(raw)
    stripped = raw.strip()
    if stripped.startswith("{") or stripped.startswith("["):
        return json.loads(stripped)
    if looks_like_html_document(stripped):
        return {"html_code": stripped}
    return {"text": stripped}


def execute_post(
    endpoint: str,
    data: bytes,
    headers: Dict[str, str],
    timeout: int,
    direct: bool = False,
) -> tuple[str, str]:
    request = urllib.request.Request(endpoint, data=data, headers=headers, method="POST")
    opener = urllib.request.build_opener(urllib.request.ProxyHandler({})) if direct else None
    open_fn = opener.open if opener else urllib.request.urlopen
    with open_fn(request, timeout=timeout) as response:
        raw = response.read().decode("utf-8", errors="replace")
        content_type = response.headers.get("Content-Type", "")
    return raw, content_type


def normalize_endpoint_url(endpoint: str) -> str:
    endpoint = endpoint.strip()
    if not endpoint:
        return ""
    if re.match(r"^https?://", endpoint, re.IGNORECASE):
        return endpoint
    host = endpoint.split("/", 1)[0].strip("[]").split(":", 1)[0].lower()
    if (
        host in {"localhost", "127.0.0.1", "::1"}
        or host.startswith("192.168.")
        or host.startswith("10.")
        or re.match(r"^172\.(1[6-9]|2\d|3[0-1])\.", host)
    ):
        return f"http://{endpoint}"
    return f"https://{endpoint}"


def normalize_chat_endpoint(endpoint: str) -> str:
    endpoint = normalize_endpoint_url(endpoint).rstrip("/")
    if endpoint.endswith("/chat/completions"):
        return endpoint
    if endpoint.endswith("/responses"):
        return endpoint
    if endpoint.endswith("/v1") or endpoint.endswith("/api/v3"):
        return endpoint + "/chat/completions"
    return endpoint


def endpoint_uses_responses(endpoint: str) -> bool:
    return normalize_endpoint_url(endpoint).rstrip("/").endswith("/responses")


def build_ai_payload(config: Dict[str, Any], system_prompt: str, user_prompt: str, temperature: float | None = None) -> Dict[str, Any]:
    model = config.get("model") or DEFAULT_INTEGRATION["model"]
    if endpoint_uses_responses(str(config.get("endpoint") or "")):
        combined_prompt = f"{system_prompt.strip()}\n\nUser input JSON:\n{user_prompt.strip()}"
        return {
            "model": model,
            "input": [
                {
                    "role": "user",
                    "content": [
                        {
                            "type": "input_text",
                            "text": combined_prompt,
                        }
                    ],
                }
            ],
        }
    payload: Dict[str, Any] = {
        "model": model,
        "messages": [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt},
        ],
    }
    if temperature is not None:
        payload["temperature"] = temperature
    return payload


def should_retry_without_proxy(endpoint: str, exc: urllib.error.URLError) -> bool:
    if is_local_endpoint(endpoint) or not is_connection_refused(exc):
        return False
    proxy = proxy_for_endpoint(endpoint)
    return bool(proxy and is_loopback_proxy(proxy))


def proxy_for_endpoint(endpoint: str) -> str:
    scheme = urllib.parse.urlparse(endpoint).scheme.lower()
    proxies = urllib.request.getproxies()
    return str(proxies.get(scheme) or proxies.get("all") or "")


def is_loopback_proxy(proxy_url: str) -> bool:
    if not proxy_url:
        return False
    normalized = proxy_url if "://" in proxy_url else f"http://{proxy_url}"
    host = (urllib.parse.urlparse(normalized).hostname or "").lower()
    return host in {"127.0.0.1", "localhost", "::1"}


def is_local_endpoint(endpoint: str) -> bool:
    host = (urllib.parse.urlparse(endpoint).hostname or "").lower()
    return host in {"127.0.0.1", "localhost", "::1"}


def is_doubao_endpoint(endpoint: str) -> bool:
    host = (urllib.parse.urlparse(endpoint).hostname or "").lower()
    return "volces.com" in host or "volcengine" in host


def effective_timeout(endpoint: str, config: Dict[str, Any]) -> int:
    timeout = normalize_timeout(config.get("timeoutSec"))
    if is_doubao_endpoint(endpoint):
        return max(timeout, 300)
    return timeout


def is_timeout_error(exc: BaseException) -> bool:
    reason = getattr(exc, "reason", None)
    text = f"{exc} {reason}".lower()
    return (
        isinstance(exc, TimeoutError)
        or isinstance(reason, (TimeoutError, socket.timeout))
        or "timed out" in text
        or "read operation timed out" in text
        or "timeout" in text
    )


def format_timeout_error(endpoint: str, timeout: int, retried_direct: bool = False) -> str:
    parsed = urllib.parse.urlparse(endpoint)
    host = parsed.netloc or endpoint
    retry_note = " after direct retry" if retried_direct else ""
    return (
        f"API request timed out{retry_note} while waiting for {host} after {timeout}s. "
        "Large PPT files and Doubao/Ark generation can take several minutes. "
        "Try again, reduce the PPT size, or increase Timeout (sec) up to 600 in Advanced connection settings."
    )


def is_connection_refused(exc: BaseException) -> bool:
    reason = getattr(exc, "reason", None)
    text = f"{exc} {reason}".lower()
    return (
        isinstance(reason, ConnectionRefusedError)
        or "10061" in text
        or "actively refused" in text
        or "connection refused" in text
        or "目标计算机积极拒绝" in text
    )


def format_url_error(endpoint: str, exc: urllib.error.URLError, retried_direct: bool = False) -> str:
    parsed = urllib.parse.urlparse(endpoint)
    host = parsed.netloc or endpoint
    reason = getattr(exc, "reason", exc)
    if is_connection_refused(exc):
        if is_local_endpoint(endpoint):
            return (
                f"API request failed: cannot connect to local endpoint {host}. "
                "Please start the workflow/API service on that port, or correct the Endpoint."
            )
        proxy = proxy_for_endpoint(endpoint)
        if proxy and is_loopback_proxy(proxy) and not retried_direct:
            return (
                f"API request failed: the Windows proxy {proxy} refused the connection while connecting to {host}. "
                "Start that proxy app, disable the system proxy, or retry after the platform bypasses the proxy."
            )
        if retried_direct:
            return (
                f"API request failed: the local proxy refused the connection and direct retry to {host} also failed. "
                f"Reason: {reason}"
            )
    return f"API request failed while connecting to {host}: {reason}"


def wrap_workflow_payload(config: Dict[str, Any], payload: Dict[str, Any]) -> Dict[str, Any]:
    payload_mode = config.get("workflowPayload") or "flat"
    if payload_mode == "input":
        return {"input": payload}
    if payload_mode == "dify":
        return {
            "inputs": payload,
            "response_mode": "blocking",
            "user": "ppt-html-studio",
        }
    return payload


def parse_custom_headers(raw: str) -> Dict[str, str]:
    raw = raw.strip()
    if not raw:
        return {}
    try:
        parsed = json.loads(raw)
    except json.JSONDecodeError:
        parsed = {}
        for line in raw.splitlines():
            line = line.strip().rstrip("\\").strip()
            header_match = re.match(r"^-H\s+['\"]?([^:'\"]+)\s*:\s*(.*?)['\"]?\s*$", line)
            if header_match:
                key, value = header_match.groups()
            elif ":" in line:
                key, value = line.split(":", 1)
            else:
                continue
            key = key.strip()
            value = value.strip().strip("'\"")
            if not is_valid_header_name(key) or not value:
                continue
            parsed[key] = value
    if not isinstance(parsed, dict):
        return {}
    return {
        str(key).strip(): str(value)
        for key, value in parsed.items()
        if is_valid_header_name(str(key).strip()) and str(value).strip()
    }


def is_valid_header_name(name: str) -> bool:
    return bool(re.fullmatch(r"[!#$%&'*+.^_`|~0-9A-Za-z-]+", name or ""))


def extract_html_from_any(value: Any) -> str:
    if isinstance(value, str):
        return extract_html(value)
    if isinstance(value, dict):
        for key in ("html_code", "html", "content", "result", "data", "output", "outputs", "output_text", "answer", "text", "body"):
            if key in value:
                found = extract_html_from_any(value[key])
                if found:
                    return found
        choices = value.get("choices")
        if isinstance(choices, list) and choices:
            for choice in choices:
                found = extract_html_from_any(choice)
                if found:
                    return found
        message = value.get("message")
        if isinstance(message, dict):
            return extract_html_from_any(message.get("content"))
    if isinstance(value, list):
        for item in value:
            found = extract_html_from_any(item)
            if found:
                return found
    return ""


def extract_html(text: str) -> str:
    text = text.strip()
    if text.startswith("{") or text.startswith("["):
        try:
            return extract_html_from_any(json.loads(text))
        except json.JSONDecodeError:
            pass
    fenced = re.search(r"```(?:html)?\s*(.*?)```", text, flags=re.IGNORECASE | re.DOTALL)
    if fenced:
        text = fenced.group(1).strip()
    start = text.lower().find("<!doctype")
    if start < 0:
        start = text.lower().find("<html")
    if start >= 0:
        end = text.lower().rfind("</html>")
        if end >= 0:
            return text[start : end + len("</html>")].strip()
        return text[start:].strip()
    if looks_like_html_fragment(text):
        return wrap_html_fragment(text)
    return text if looks_like_html_document(text) else ""


def looks_like_html_document(text: str) -> bool:
    lowered = text.strip().lower()
    return "<!doctype" in lowered or "<html" in lowered


def looks_like_html_fragment(text: str) -> bool:
    lowered = text.strip().lower()
    if not lowered or lowered.startswith("{") or lowered.startswith("["):
        return False
    tags = (
        "<style",
        "<script",
        "<main",
        "<section",
        "<article",
        "<div",
        "<body",
        "<h1",
        "<figure",
        "<img",
        "<table",
    )
    return any(tag in lowered for tag in tags) and bool(re.search(r"</?[a-z][\w:-]*(?:\s|>|/>)", lowered))


def wrap_html_fragment(fragment: str) -> str:
    lowered = fragment.lower()
    title = "AI Generated Presentation"
    style_blocks = "\n".join(re.findall(r"<style\b[^>]*>.*?</style>", fragment, flags=re.IGNORECASE | re.DOTALL))
    script_blocks = "\n".join(re.findall(r"<script\b[^>]*>.*?</script>", fragment, flags=re.IGNORECASE | re.DOTALL))
    body = re.sub(r"<style\b[^>]*>.*?</style>", "", fragment, flags=re.IGNORECASE | re.DOTALL)
    body = re.sub(r"<script\b[^>]*>.*?</script>", "", body, flags=re.IGNORECASE | re.DOTALL).strip()
    if "<body" in lowered:
        body_match = re.search(r"<body\b[^>]*>(.*?)</body>", fragment, flags=re.IGNORECASE | re.DOTALL)
        if body_match:
            body = body_match.group(1).strip()
    return (
        "<!DOCTYPE html>\n"
        "<html lang=\"en\">\n"
        "<head>\n"
        "  <meta charset=\"UTF-8\">\n"
        "  <meta name=\"viewport\" content=\"width=device-width, initial-scale=1.0\">\n"
        f"  <title>{title}</title>\n"
        f"{style_blocks}\n"
        "</head>\n"
        f"<body>\n{body}\n{script_blocks}\n</body>\n"
        "</html>"
    )


def extract_slides_from_any(value: Any) -> List[Dict[str, Any]]:
    if isinstance(value, str):
        text = value.strip()
        fenced = re.search(r"```(?:json)?\s*(.*?)```", text, flags=re.IGNORECASE | re.DOTALL)
        if fenced:
            text = fenced.group(1).strip()
        if text.startswith("{") or text.startswith("["):
            try:
                return extract_slides_from_any(json.loads(text))
            except json.JSONDecodeError:
                return []
        return []
    if isinstance(value, list):
        if all(isinstance(item, dict) for item in value) and any("page" in item for item in value):
            return value
        for item in value:
            found = extract_slides_from_any(item)
            if found:
                return found
        return []
    if isinstance(value, dict):
        for key in ("optimized_slides", "slides", "ppt_content", "pages"):
            found = extract_slides_from_any(value.get(key))
            if found:
                return found
        for key in ("content", "result", "data", "output", "outputs", "output_text", "answer", "text", "body", "html_code", "html"):
            if key in value:
                found = extract_slides_from_any(value[key])
                if found:
                    return found
        choices = value.get("choices")
        if isinstance(choices, list):
            for choice in choices:
                found = extract_slides_from_any(choice)
                if found:
                    return found
        message = value.get("message")
        if isinstance(message, dict):
            return extract_slides_from_any(message.get("content"))
    return []


