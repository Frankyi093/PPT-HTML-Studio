def load_integration_config(config_path: Path) -> Dict[str, Any]:
    if not config_path.exists():
        return dict(DEFAULT_INTEGRATION)
    try:
        data = json.loads(config_path.read_text(encoding="utf-8-sig"))
    except Exception:
        return dict(DEFAULT_INTEGRATION)
    config = dict(DEFAULT_INTEGRATION)
    for key in DEFAULT_INTEGRATION:
        if key in data:
            config[key] = data[key]
    config["timeoutSec"] = normalize_timeout(config.get("timeoutSec"))
    config["fallbackToLocal"] = bool(config.get("fallbackToLocal", True))
    if config.get("mode") not in {"local", "workflow_api", "ai_api"}:
        config["mode"] = "local"
    if config.get("apiKeyHeader") not in {"Authorization", "X-API-Key", "api-key", "none"}:
        config["apiKeyHeader"] = "Authorization"
    if config.get("workflowPayload") not in {"flat", "input", "dify"}:
        config["workflowPayload"] = "flat"
    return config


def save_integration_config(config_path: Path, patch: Dict[str, Any]) -> Dict[str, Any]:
    current = load_integration_config(config_path)
    if patch.get("clearApiKey"):
        current["apiKey"] = ""
    for key in DEFAULT_INTEGRATION:
        if key in patch:
            value = patch[key]
            if key == "apiKey" and value == "":
                continue
            current[key] = value
    current["timeoutSec"] = normalize_timeout(current.get("timeoutSec"))
    current["fallbackToLocal"] = bool(current.get("fallbackToLocal", True))
    if current.get("mode") not in {"local", "workflow_api", "ai_api"}:
        current["mode"] = "local"
    if current.get("apiKeyHeader") not in {"Authorization", "X-API-Key", "api-key", "none"}:
        current["apiKeyHeader"] = "Authorization"
    if current.get("workflowPayload") not in {"flat", "input", "dify"}:
        current["workflowPayload"] = "flat"
    config_path.parent.mkdir(parents=True, exist_ok=True)
    config_path.write_text(json.dumps(current, ensure_ascii=False, indent=2), encoding="utf-8-sig")
    return current


def public_integration_config(config: Dict[str, Any]) -> Dict[str, Any]:
    public = {key: config.get(key, DEFAULT_INTEGRATION[key]) for key in DEFAULT_INTEGRATION if key != "apiKey"}
    key = str(config.get("apiKey") or "")
    public["hasApiKey"] = bool(key)
    public["apiKeyMasked"] = mask_key(key)
    return public


def mask_key(key: str) -> str:
    if not key:
        return ""
    if len(key) <= 8:
        return "*" * len(key)
    return f"{key[:4]}...{key[-4:]}"


def normalize_timeout(value: Any) -> int:
    try:
        timeout = int(value)
    except Exception:
        timeout = 90
    return max(10, min(timeout, 600))


def should_use_external(config: Dict[str, Any]) -> bool:
    return config.get("mode") in {"workflow_api", "ai_api"} and bool(str(config.get("endpoint") or "").strip())


